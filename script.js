// 브라우저 언어 감지
function detectBrowserLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    
    if (browserLang.startsWith('ko')) {
        return 'ko';
    } else if (browserLang.startsWith('ja')) {
        return 'ja';
    } else {
        return 'en';
    }
}

